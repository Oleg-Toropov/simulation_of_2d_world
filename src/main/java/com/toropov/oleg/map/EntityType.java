package com.toropov.oleg.map;

/**
 * Enumeration representing different types of entities in the simulation.
 */
public enum EntityType {
    /**
     * Represents a rooster.
     */
    ROOSTER,

    /**
     * Represents a hen.
     */
    HEN,

    /**
     * Represents grass.
     */
    GRASS,

    /**
     * Represents a male fox.
     */
    MALE_FOX,

    /**
     * Represents a female fox.
     */
    FEMALE_FOX,

    /**
     * Represents a fox cub.
     */
    FOX_CUB,

    /**
     * Represents a chick.
     */
    CHICK,

    /**
     * Represents all chicken types combined.
     */
    ALL_CHICKEN,

    /**
     * Represents all fox types combined.
     */
    ALL_FOXES
}
